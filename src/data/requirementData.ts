- 若单次回答引用了多个知识片段，在回答末尾列出全部引用来源清单

引用溯源的业务价值：
- 用户可据此判断信息的时效性和权威性
- 运营人员可通过引用链路追溯问题回答的知识来源
- 若用户对回答不满意，可快速定位到出问题的知识片段进行修正

